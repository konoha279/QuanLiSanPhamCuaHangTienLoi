    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        List<PhieuNhapHang> DanhSachPN = new DanhSachPhieuNhap().getInstance().getDanhSachPhieuNhap();
        model = (DefaultTableModel) tblBangPhieuNhap.getModel();
        DefaultTableModel model = (DefaultTableModel) tblBangPhieuNhap.getModel();
        while( model.getRowCount()!= 0)
        {
            model.removeRow(0);
        }
        tblBangPhieuNhap.setModel(model);
        Object[] row =new Object[6];
        for(int i=0; i< DanhSachPN.size();i++)
        {
            if (DanhSachPN.get(i).getMaPhieuNhap().indexOf(txtTimKiem.getText())!= -1 ||DanhSachPN.get(i).getMaSanPham().indexOf(txtTimKiem.getText())!=-1)

            {
                row[0]= DanhSachPN.get(i).getMaPhieuNhap();
                row[1]=DanhSachPN.get(i).getMaSanPham();
                row[2]=DanhSachPN.get(i).getSoLuongSP();
                row[3]=DanhSachPN.get(i).getNgayLap();
                row[3]=DanhSachPN.get(i).getCMND_NhanVien();
                row[3]=DanhSachPN.get(i).getMaNhaCungCap();

                model.addRow(row);
            }

        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

