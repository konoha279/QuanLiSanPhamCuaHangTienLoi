    void LoadTable()
    {
        model = (DefaultTableModel) tblBangPhieuNhap.getModel();
        tblBangPhieuNhap.setModel(model);    
        List<PhieuNhapHang> DanhSachPN = new DanhSachPhieuNhap().getInstance().getDanhSachPhieuNhap();
        Object[] row = new Object[6];
        for (int i=0; i< DanhSachPN.size(); i++)
        {
            row[0] = DanhSachPN.get(i).getMaPhieuNhap();
            row[1] = DanhSachPN.get(i).getMaSanPham();
            row[2] = DanhSachPN.get(i).getSoLuongSP();
            row[3] = DanhSachPN.get(i).getNgayLap();
            row[4] = DanhSachPN.get(i).getCMND_NhanVien();
            row[5] = DanhSachPN.get(i).getMaNhaCungCap();
            
            model.addRow(row);
        }
        
    }
    
    void ClearTable()
    {
        while (model.getRowCount() != 0)
        {
            model.removeRow(0);

        }
    }
    
    void Refresh()
    {
        ClearTable();
        LoadTable();
    }
    


